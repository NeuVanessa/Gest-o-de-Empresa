
package br.unirb.modelo;


public class Agenda {
    
    
    private String datCriAge;
    private String datComAge;
    private long codColAge;
    private long codEquAge;
    private String titAge;
    private String desAge;
    private long idCol;
    private long idEqu;

    public String getDatCriAge() {
        return datCriAge;
    }

    public void setDatCriAge(String datCriAge) {
        this.datCriAge = datCriAge;
    }

    public String getDatComAge() {
        return datComAge;
    }

    public void setDatComAge(String datComAge) {
        this.datComAge = datComAge;
    }

    public long getCodColAge() {
        return codColAge;
    }

    public void setCodColAge(long codColAge) {
        this.codColAge = codColAge;
    }

    public long getCodEquAge() {
        return codEquAge;
    }

    public void setCodEquAge(long codEquAge) {
        this.codEquAge = codEquAge;
    }

    public String getTitAge() {
        return titAge;
    }

    public void setTitAge(String titAge) {
        this.titAge = titAge;
    }

    public String getDesAge() {
        return desAge;
    }

    public void setDesAge(String desAge) {
        this.desAge = desAge;
    }

    public long getIdCol() {
        return idCol;
    }

    public void setIdCol(long idCol) {
        this.idCol = idCol;
    }

    public long getIdEqu() {
        return idEqu;
    }

    public void setIdEqu(long idEqu) {
        this.idEqu = idEqu;
    }
    
    
}
